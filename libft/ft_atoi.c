#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	long long	num;
	long long	sign;

	i = 0;
	num = 0;
	sign = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (sign * (str[i] - '0'));
		if (num > 2147483647)
			return (-1);
		if (num < -2147483648)
			return (0);
		i++;
	}
	return (num);
}
